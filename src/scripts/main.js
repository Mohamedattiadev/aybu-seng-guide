// Theme toggle (dark ↔ light, starting from effective state)
const THEME_KEY = 'aybu-theme';
function effectiveTheme(stored) {
  if (stored === 'dark' || stored === 'light') return stored;
  return matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';
}
function applyTheme(t) {
  document.documentElement.setAttribute('data-theme', t);
  localStorage.setItem(THEME_KEY, t);
  const btn = document.getElementById('themeToggle');
  if (btn) {
    btn.textContent = t === 'dark' ? '☀' : '☾';
    btn.title = 'Theme: ' + t;
  }
}
document.getElementById('themeToggle')?.addEventListener('click', () => {
  const cur = effectiveTheme(localStorage.getItem(THEME_KEY));
  applyTheme(cur === 'dark' ? 'light' : 'dark');
});
applyTheme(effectiveTheme(localStorage.getItem(THEME_KEY)));

