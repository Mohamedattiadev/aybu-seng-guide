function openCourseModal(tr) {
  const cb = tr.querySelector('input[type="checkbox"][data-track]');
  if (!cb) return;
  const cells = tr.querySelectorAll('td');
  // cells: [0]=chk, [1]=code, [2]=name, [3]=T, [4]=U, [5]=L, [6]=ECTS, [7]=prereq
  const code = cells[1].textContent.trim();
  const name = cells[2].textContent.trim();
  const T = cells[3].textContent.trim();
  const U = cells[4].textContent.trim();
  const L = cells[5].textContent.trim();
  const ects = cells[6].textContent.trim();
  const prereq = cells[7].textContent.trim();
  // find semester from preceding h3 inside same card
  let sem = '—';
  let prev = tr.closest('.table-wrap').previousElementSibling;
  while (prev) {
    if (prev.tagName === 'H3') { sem = prev.textContent.trim(); break; }
    prev = prev.previousElementSibling;
  }
  const cat = cb.dataset.cat || 'mandatory';
  document.getElementById('cmCode').textContent = code;
  document.getElementById('cmName').textContent = name;
  const lang = localStorage.getItem('aybu-lang') || 'en';
  const d = i18n[lang] || i18n.en;
  document.getElementById('cmInfo').innerHTML = `
    <div class="info-card"><div class="label">${d['modal.semester']}</div><div class="val">${sem}</div></div>
    <div class="info-card"><div class="label">T-U-L-ECTS</div><div class="val">${T}-${U}-${L}-${ects}</div></div>
    <div class="info-card"><div class="label">${d['modal.prereq']}</div><div class="val">${prereq}</div></div>
    <div class="info-card"><div class="label">${d['modal.category']}</div><div class="val">${cat}</div></div>
  `;
  modal.classList.add('open');
}
function closeCourseModal() { modal.classList.remove('open'); }
