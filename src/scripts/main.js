const PROGRESS_KEY = 'aybu-progress-v1';
const GRADES_KEY = 'aybu-grades-v1';
const REQ = { ects: 240, dept: 50, facuni: 6, intern: 2, project: 2 };
const GRADES = { AA:4, BA:3.5, BB:3, CB:2.5, CC:2, DC:1.5, DD:1, FD:0.5, FF:0 };
let progress = {}, grades = {};
try { progress = JSON.parse(localStorage.getItem(PROGRESS_KEY) || '{}') || {}; } catch(e) {}
try { grades = JSON.parse(localStorage.getItem(GRADES_KEY) || '{}') || {}; } catch(e) {}

function getCheckables() {
  return document.querySelectorAll('input[type="checkbox"][data-track]');
}
function applyChecked() {
  getCheckables().forEach(cb => {
    cb.checked = !!progress[cb.dataset.code];
    const row = cb.closest('tr, .elective-card');
    if (row) row.classList.toggle('completed', cb.checked);
  });
  document.querySelectorAll('select.grade-sel').forEach(sel => {
    sel.value = grades[sel.dataset.gradeCode] || '';
  });
}
function computeTotals() {
  const t = { ects:0, dept:0, facuni:0, intern:0, project:0, gno:null };
  getCheckables().forEach(cb => {
    if (!cb.checked) return;
    const ects = parseFloat(cb.dataset.ects) || 0;
    const cat = cb.dataset.cat;
    t.ects += ects;
    if (cat === 'dept-elective') t.dept += ects;
    else if (cat === 'fac-elective') t.facuni += ects;
    else if (cat === 'internship') t.intern += 1;
    else if (cat === 'project') t.project += 1;
  });
  let we=0, ge=0;
  Object.keys(grades).forEach(code => {
    const g = grades[code];
    if (!g || !(g in GRADES) || !progress[code]) return;
    const cb = document.querySelector(`input[data-track][data-code="${code}"]`);
    if (!cb) return;
    const ects = parseFloat(cb.dataset.ects) || 0;
    we += ects * GRADES[g];
    ge += ects;
  });
  t.gno = ge ? we/ge : null;
  return t;
}
